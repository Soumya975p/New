public class Q4 {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("Before Swap");
        System.out.println(a);
        System.out.println(b);
        int t;
        t=a;
        a=b;
        b=t;
        System.out.println("After Swap");
        System.out.println(a);
        System.out.println(b);
    }
    
}
